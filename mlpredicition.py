# HELPER: BUILD HEATMAP WITH KERALA MASK
# =========================================
def build_heatmap(df_map, title):
    m = folium.Map(location=[10.5, 76.0], zoom_start=7)

    # Add heatmap with large radius to fill Kerala
    heat_data = [
        [row['latitude'], row['longitude'], row['AQI_norm']]
        for _, row in df_map.iterrows()
    ]
    HeatMap(
        heat_data,
        radius=60,    # large enough to fill gaps between sparse ERA5 points
        blur=50,      # smooth blending across the state
        min_opacity=0.4,
        max_zoom=10
    ).add_to(m)

    # Mask everything OUTSIDE Kerala with white
    from shapely.geometry import mapping, box
    from shapely.ops import unary_union

    world = box(-180, -90, 180, 90)
    kerala_shape = unary_union(kerala.geometry.values)
    outside = world.difference(kerala_shape)

    folium.GeoJson(
        {"type": "Feature", "geometry": mapping(outside), "properties": {}},
        style_function=lambda x: {
            'fillColor': 'white',
            'fillOpacity': 1.0,
            'color': 'none',
            'weight': 0
        }
    ).add_to(m)

    # Kerala border on top
    folium.GeoJson(
        kerala,
        style_function=lambda x: {
            'fillColor': 'none',
            'color': 'blue',
            'weight': 2
        }
    ).add_to(m)

    title_html = f"""
    <div style="position:fixed; top:10px; left:50%; transform:translateX(-50%);
                background:white; padding:8px 16px; border-radius:6px;
                font-size:15px; font-weight:bold; z-index:9999;
                box-shadow:2px 2px 6px rgba(0,0,0,0.3);">
        {title}
    </div>"""
    m.get_root().html.add_child(folium.Element(title_html))
    return m
