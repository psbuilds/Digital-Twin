# =========================================
# SHARED NORMALISATION (same scale = fair comparison)
# =========================================
global_min = min(actual_df['Final_AQI'].min(), pred_df['Final_AQI'].min())
global_max = max(actual_df['Final_AQI'].max(), pred_df['Final_AQI'].max())

def normalise(series):
    return (series - global_min) / (global_max - global_min)

actual_df['AQI_norm'] = normalise(actual_df['Final_AQI'])
pred_df['AQI_norm']   = normalise(pred_df['Final_AQI'])

